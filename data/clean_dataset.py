import pandas as pd

# Load Excel file
df = pd.read_excel("default of credit card clients.xls", header=1)

# Rename target column (important for our code)
df.rename(columns={"default payment next month":
                   "default.payment.next.month"}, inplace=True)

# Drop ID column (not a feature)
df.drop(columns=["ID"], inplace=True)

# Check basic info
print("Shape:", df.shape)
print("Columns:", df.columns.tolist())
print("\nMissing values:\n", df.isnull().sum())

# Save cleaned CSV
df.to_csv("credit_card.csv", index=False)

print("\nCleaned dataset saved as credit_card.csv")